package model;

/**
 *
 * @author vinicius
 */
public class Cargo {
    
    private int idCargo;
    private String descricao;
    
    public Cargo(){  
    }
    
    public Cargo(int id, String descricao){
        this.idCargo = id;
        this.descricao = descricao;
    }

    public int getIdCargo() {
        return idCargo;
    }

    public void setIdCargo(int idCargo) {
        this.idCargo = idCargo;
    }

    public String getDescricao() {
        return descricao;
    }

    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    @Override
    public int hashCode() {
        int hash = 3;
        hash = 41 * hash + this.idCargo;
        return hash;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final Cargo other = (Cargo) obj;
        if (this.idCargo != other.idCargo) {
            return false;
        }
        return true;
    }
    
    
    
}
